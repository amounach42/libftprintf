#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*dup;
	int		i;

	ft_strlen (s1);
	i = 0;
	dup = (char *)malloc(sizeof(char) * ft_strlen(s1) + 1);
	if (dup == NULL)
		return (NULL);
	while (s1[i])
	{
		dup[i] = s1[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
